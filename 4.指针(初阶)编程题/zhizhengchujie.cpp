//第7题(编程题)
//题目名称:
//打印水仙花数
//题目内容:
//求所有"水仙花数"并输出。
//"水仙花数"是指一个n位数，其各位数字的n次方之和确好等于该数本身， 
//如:153=1^3+5^3+3^3，则153是一个"水仙花数"。
#include<stdio.h>
int main()
{
	int a,b,c;
	for(int i=100;i<=999;i++)
	{
		a=i/100;  //百位数 
		b=(i/10)%10;  //十位数 
		c=i%10;  //个位数 
		if(a*a*a+b*b*b+c*c*c==i) 
		printf("%d\n",i);
	}
 	return 0;
}  


